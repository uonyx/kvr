int main () //int argc, char* argv [])
{
  kvr::ctx *ctx = kvr::ctx::create ();

  kvr::value *map = ctx->create_value ()->as_map ();

  map->insert ("int", (int64_t) 16);
  map->insert ("float", 3.14);
  map->insert ("string", "hello world");
  map->insert ("boolean", true);

  int64_t i = map->find ("int")->get_integer ();
  (void) i;
  // printf ("int: %lld\n", i);

  ctx->destroy_value (map);

  kvr::ctx::destroy (ctx);

  return 0;
}

